function pickPool(day) {
  return [...QUEST_POOL]
    .map((quest, index) => ({
      quest,
      sort: Math.sin((day + 1) * (index + 3) * 12.9898) * 43758.5453,
    }))
    .sort((a, b) => (a.sort - Math.floor(a.sort)) - (b.sort - Math.floor(b.sort)))
    .map((entry) => entry.quest);
}

export function rollQuestNotices(day, count = 3) {
  return pickPool(day).slice(0, count).map((quest, index) => ({
    ...quest,
    noticeId: `${quest.id}-${day}-${index}`,
    posted: false,
  }));
}
